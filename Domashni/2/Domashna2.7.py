""" Вежба 7: Напиши програма за пребројување и печатење на бројот на парни броеви во листа."""


lista = [1,3,6,9,12,18,26,37,44,57,68,73,82,94]
cnt = 0
lista_par = []

for i in lista:
    if i%2 == 0:
        cnt = cnt + 1
        lista_par.append(i)
print(f'Вкупниот број на парни броеви во листата е {cnt} И тие се {lista_par} ')