""" Вежба 12: Напиши програма што ќе бара од корисникот да внесе серија на броеви. 
Зачувај ги броевите во листа и потоа испечати го најголемиот број во листата."""

lista = []
x= int(input('Внесте број колку сакате да биде долга вашата листа: '))
cnt = 1

for i in range(x):
    y = int(input(f'Внесете те го {cnt} број : '))
    lista.append(y)
    cnt = cnt + 1 

print(lista)
print(f'Максималниот број во листата е {max(lista)}')