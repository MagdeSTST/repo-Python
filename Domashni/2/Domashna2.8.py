""" Вежба 8: Напиши програма за пребројување и печатење на бројот на непарни броеви во листа."""
lista = [1,3,6,9,12,18,26,37,44,57,68,73,82,94]

cnt = 0 
lista_nep = []
for i in lista:
    if i%2 != 0:
        cnt = cnt + 1
        lista_nep.append(i)
print(f'Вкупниот број на непарни броеви во листата е {cnt} И тие се {lista_nep} ')