""" Вежба 16: Напиши програма што ќе бара од корисникот да внесе серија на броеви. З
ачувај ги броевите во листа и потоа испечати само парните броеви од листата."""

lista = []
lista_pari = []

x = int(input('Колкав броеви сакате да внесет: '))
cnt = 1
for i in range(x):
    y = int(input(f'Внесете го {cnt} број: '))
    lista.append(y)
    if y%2 == 0:
        lista_pari.append(y)
    cnt = cnt +1

print(f'Целата листа е {lista}')
print(f'Листата со парни броеви е {lista_pari}')