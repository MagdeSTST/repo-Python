""" Вежба 4, 5 и 6 (комбинирана): Напиши програма за наоѓање и печатење на најголемиот, најмалиот и просекот на листа од броеви."""

lista = []

for i in range(1,11):
    x = int(input(f'Внесете рандом 10 броеви, број {i} :'))
    lista.append(x)

ma = max(lista) 
mi = min(lista)
a = int(sum(lista)/len(lista))

print(f'Во листата што ја внесовте  {lista} \n најголемиот број е {ma} ; \n најмалиот број е {mi} ; \n просекот е {a}')